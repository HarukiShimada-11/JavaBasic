public class PTra02_01 {
	public static void main(String[] args) {

		// ★ 20を5で割った値を出力してください
		int i=20;
		System.out.println(i/5);


		// ★ 126に13を掛けた値を出力してください
		int a=126;
		System.out.println(a*13);


		// ★ 97を6で割ったときの余りを出力してください
		float s=97.0f;
		System.out.println(s/6);


	}
}
